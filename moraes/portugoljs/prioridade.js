function calcularOperacoes() {
    let resultado;

    resultado = 6 + 3 * 5;
    console.log("Operação: 6 + 3 * 5 = " + resultado);
   
    resultado = (6 + 3) * 5;
    console.log("Operação: (6 + 3) * 5 = " + resultado);

    resultado = 2 + 8 / 4 * 7;
    console.log("Operação: 2 + 8 / 4 * 7 = " + resultado);

    resultado = (2 + 8) / (4 * 7);
    console.log("Operação: (2 + 8) / (4 * 7) = " + resultado);
}

calcularOperacoes();
